export interface SpeciesProfile {
  family: string;
  size: string;
  habitat: string;
  range: string;
  diet: string;
  summary: string;
  field_marks: string[];
  curated: boolean;
}

function p(
  family: string,
  size: string,
  habitat: string,
  range: string,
  diet: string,
  summary: string,
  field_marks: string[],
): SpeciesProfile {
  return { family, size, habitat, range, diet, summary, field_marks, curated: true };
}

function fallbackProfile(species: CatalogSpecies): SpeciesProfile {
  return {
    family: species.family,
    size: "",
    habitat: "",
    range: "",
    diet: "",
    summary: `${species.species} is one of the bird species Burd can identify from photos. Ask below to learn about its habits, range, and field marks.`,
    field_marks: [],
    curated: false,
  };
}

