export function getCuratedSpeciesProfile(id: string): SpeciesProfile | null {
  return SPECIES_PROFILES[id] ?? null;
}

export function getSpeciesProfile(
  id: string,
  species?: CatalogSpecies,
): SpeciesProfile {
  const rich = SPECIES_PROFILES[id];
  if (rich) return rich;

  const catalogSpecies = species ?? getCatalogSpeciesById(id);
  if (!catalogSpecies) {
    return {
      family: "Unknown",
      size: "",
      habitat: "",
      range: "",
      diet: "",
      summary: "",
      field_marks: [],
      curated: false,
    };
  }

  return fallbackProfile(catalogSpecies);
}
